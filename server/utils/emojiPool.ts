export function getRandomEmojis(count: number): string[] {
  const poolCopy = [...GLOBAL_EMOJI_POOL];

  for (let i = poolCopy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));

    const itemI = poolCopy[i] as string;
    const itemJ = poolCopy[j] as string;

    poolCopy[i] = itemJ;
    poolCopy[j] = itemI;
  }

  return poolCopy.slice(0, count);
}
